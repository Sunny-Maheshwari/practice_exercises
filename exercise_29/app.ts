// Array of favorite pizza names
let pizzas = ['Margherita', 'Pepperoni', 'Supreme'];

for (let pizza of pizzas) {
    console.log(pizza);
}

console.log("\n");

for (let i = 0; i < pizzas.length; i++) {
    console.log(`I like ${pizzas[i]} pizza.`);
}

console.log("\n");

console.log("I really love pizza!");
